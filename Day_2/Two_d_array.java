package Day_2;

// 2D Array - it can store multiple Arrays in array. 
//Example - we can store every student marks w.r.t there subject in 2D Array.
import java.util.*;;

public class Two_d_array {

    public static void main(String[] args){
        int[][] marks = {{98,97,95},{9,4,5}};
        System.out.println(marks[0][0]);
        System.out.println(marks[0][1]);
        System.out.println(marks[0][2]);
        System.out.println(marks[1][0]);
        System.out.println(marks[1][1]);
        System.out.println(marks[1][2]);

    
    }
}
